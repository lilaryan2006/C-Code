#include <stdio.h>

int main()
{
    int a, b;

    printf("Enter the two numbers to perform the Arithmetic Operations\n", a, b);
    scanf("%d%d", &a, &b);

    printf("a + b = %d\n", a + b);
    printf("a - b = %d\n", a - b);
    printf("a * b = %d\n", a * b);
    printf("a / b = %d\n", a / b);

    return 0;
}
